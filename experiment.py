import numpy as np
from scipy.stats import spearmanr, kendalltau

def compute_metrics(orig, perturbed, perturbation_name, model_name):
    """Compute consistency metrics."""
    pairs = [(o, p) for o, p in zip(orig, perturbed) if o is not None and p is not None]
    if not pairs:
        return None

    orig_scores = [p[0] for p in pairs]
    pert_scores = [p[1] for p in pairs]

    exact_match = sum(1 for o, p in pairs if o == p) / len(pairs)
    mean_abs_diff = np.mean([abs(o - p) for o, p in pairs])
    spearman, _ = spearmanr(orig_scores, pert_scores)
    kendall, _ = kendalltau(orig_scores, pert_scores)

    # Category flips (good 4-5 to bad 1-2 or vice versa)
    flips = sum(1 for o, p in pairs if (o >= 4 and p <= 2) or (o <= 2 and p >= 4)) / len(pairs)

    print(f"\n{model_name} - {perturbation_name}:")
    print(f"  Exact match: {exact_match:.2%}")
    print(f"  Mean abs diff: {mean_abs_diff:.2f}")
    print(f"  Spearman: {spearman:.3f}")
    print(f"  Kendall: {kendall:.3f}")
    print(f"  Category flips: {flips:.2%}")

    return {
        "model": model_name,
        "perturbation": perturbation_name,
        "exact_match": exact_match,
        "mean_abs_diff": mean_abs_diff,
        "spearman": spearman,
        "kendall": kendall,
        "category_flips": flips,
        "n": len(pairs)
    }

