from scipy import stats
import numpy as np

# Load data
df = pd.read_csv("/content/judge_consistency_100.csv")

def run_tests(orig, perturbed, perturbation_name, model_name):
    """Run paired t-test and Wilcoxon signed-rank test."""
    # Remove NaN
    pairs = [(o, p) for o, p in zip(orig, perturbed) if pd.notna(o) and pd.notna(p)]
    orig_scores = [p[0] for p in pairs]
    pert_scores = [p[1] for p in pairs]

    # Paired t-test
    t_stat, t_pvalue = stats.ttest_rel(orig_scores, pert_scores)

    # Wilcoxon signed-rank test (non-parametric)
    w_stat, w_pvalue = stats.wilcoxon(orig_scores, pert_scores)

    # McNemar's test for exact match (binary: match vs no match)
    matches = [1 if o == p else 0 for o, p in pairs]
    no_match_rate = 1 - np.mean(matches)

    print(f"\n{model_name} - {perturbation_name}:")
    print(f"  Paired t-test: t={t_stat:.3f}, p={t_pvalue:.4f}")
    print(f"  Wilcoxon test: W={w_stat:.3f}, p={w_pvalue:.4f}")
    print(f"  No-match rate: {no_match_rate:.2%}")

    return {
        "model": model_name,
        "perturbation": perturbation_name,
        "t_stat": t_stat,
        "t_pvalue": t_pvalue,
        "w_stat": w_stat,
        "w_pvalue": w_pvalue,
        "no_match_rate": no_match_rate,
        "n": len(pairs)
    }

